## Learning how to use variables of different kinds
x = 1
y = 1.2

a = x + y 
b = x - y 
c = x * y 
d = x / y 

## % // - This is the floor division operator. It divides the left operand by the right operand and rounds the result down to the nearest whole number.
e = x // y 

## % - This is the modulus operator.
f = x % y

## ** - This is the exponentiation operator.
g = x ** y 

x_equals_y = (x == y)

z = "Commandar Erwin Smith"

print (a, b, c, d, e, f, g, z, sep="\n")